package library.vo;

public class RentbookVO {
	private String rent_num;
	private int book_num;
	private String ret_date;
	private String ret_con;
	
	
	public String getRent_num() {
		return rent_num;
	}
	public void setRent_num(String rent_num) {
		this.rent_num = rent_num;
	}
	public int getBook_num() {
		return book_num;
	}
	public void setBook_num(int book_num) {
		this.book_num = book_num;
	}
	public String getRet_date() {
		return ret_date;
	}
	public void setRet_date(String ret_date) {
		this.ret_date = ret_date;
	}
	public String getRet_con() {
		return ret_con;
	}
	public void setRet_con(String ret_con) {
		this.ret_con = ret_con;
	}
	
	
	
	
}
